int ColorfulWolves::getmin(vector <string> colormap) {
	int n = colormap.size();
	for (int i = 0; i < n; i++) for (int j = 0; j < n; j++) d[i][j] = 1061109567;
	for (int i = 0; i < n; i++){
		int cnt = 0;
		for (int j = 0; j < n; j++) if (colormap[i][j] == 'Y'){
			d[i][j] = cnt;
			cnt++;
		}
	}
	for (int k = 0; k < n; k++) for (int i = 0; i < n; i++) for (int j = 0; j < n; j++)
		d[i][j] = min(d[i][j], d[i][k] + d[k][j]);
	return (d[0][n - 1] == 1061109567) ? -1 : d[0][n - 1];
}

